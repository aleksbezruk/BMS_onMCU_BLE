/**
 * @note Default config for pin, 
 * typically the config is used to reset pin configuration (De-init).
 */
static const cy_stc_gpio_pin_config_t default_config =
{
    .outVal = 0,
    .driveMode = CY_GPIO_DM_ANALOG,
    .hsiom = HSIOM_SEL_GPIO,
    .intEdge = CY_GPIO_INTR_DISABLE,
    .intMask = 0UL,
    .vtrip = CY_GPIO_VTRIP_CMOS,
    .slewRate = CY_GPIO_SLEW_SLOW,
    .driveSel = CY_GPIO_DRIVE_1_8,
    .vregEn = 0UL,
    .ibufMode = 0UL,
    .vtripSel = 0UL,
    .vrefSel = 0UL,
    .vohSel = 0UL,
};

