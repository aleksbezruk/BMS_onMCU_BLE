/**
 * @brief Initialize a pin with settings listed below. 
 * 
 * @param[in]   port    pointer to pin's Port data structure
 * 
 * @param[in]   pin     pin to init
 * 
 * @param[in]   func    pin function
 * 
 * @param[in]   pullRes pull resistor config
 * 
 * @param[in]   driveMode   output pin current drive setting
 * 
 * @param[in]   initialState    initial state LOW or HIGH
 * 
 * @note    \ref init_cycfg_pins() - Cypress autogenerated function + \ref Cy_GPIO_Pin_Init
 * 
 * @details High-impedance drive mode with input buffer disabled is also the default pin reset state.
 *          'Strong Drive' mode is used for digital output signals or to drive external devices.
 * 
 * @attention If a pin is connected only to an analog signal, the input
 *            buffer should be disabled to avoid crowbar currents.
 * 
 * @retval None
 * 
 */
void HAL_GPIO_init_pin(
    Hal_gpio_port_t* port, 
    Hal_gpio_pin_t pin,
    Hal_gpio_function_t func,
    Hal_gpio_pullRes_t pullRes,
    Hal_gpio_driveMode_t driveMode,
    Hal_gpio_pin_state_t initialState
)
{
    HAL_ASSERT(port != NULL);
    HAL_ASSERT(func <= HAL_GPIO_UART_TX);
    HAL_ASSERT(pullRes <= HAL_GPIO_PULL_DOWN);
    HAL_ASSERT(driveMode <= HAL_GPIO_DRIVE_HIGH);
    HAL_ASSERT(initialState <= HAL_GPIO_HIGH_LEVEL);

    cy_stc_gpio_pin_config_t pin_config;
    pin_config.outVal = initialState;

    /** Configure pin output/input buffer */
    if (driveMode == HAL_GPIO_DRIVE_HIGH) {
        // 'Strong Drive' output mode, without Pull resistors
        pin_config.driveMode = CY_GPIO_DM_STRONG_IN_OFF;
    } else if (driveMode == HAL_GPIO_DRIVE_NORMAL){
        // Config Pull-Up/Down for Output
        HAL_ASSERT(pullRes != HAL_GPIO_PULL_DISABLED);
        pin_config.driveMode = (pullRes ==HAL_GPIO_PULL_UP)? CY_GPIO_DM_PULLUP_IN_OFF: CY_GPIO_DM_PULLDOWN_IN_OFF;
    } else {
        // No output drive -> Input
        pin_config.driveMode = _get_inputBuf_config(func, pullRes);
    }

    /** Configure pin function (HSIOM) */
    pin_config.hsiom = _get_pinFunc_config(func);

    /** Configure other properties */
    pin_config.intEdge = CY_GPIO_INTR_DISABLE;
    pin_config.intMask = 0UL;
    pin_config.vtrip = CY_GPIO_VTRIP_CMOS;
    pin_config.slewRate = CY_GPIO_SLEW_FAST;
    pin_config.driveSel = CY_GPIO_DRIVE_1_2;
    pin_config.vregEn = 0UL;
    pin_config.ibufMode = 0UL;
    pin_config.vtripSel = 0UL;
    pin_config.vrefSel = 0UL;
    pin_config.vohSel = 0UL;

    /** Write configuration to the Registers */
    Cy_GPIO_Pin_Init(port, pin, &pin_config);
}

/**
 * @brief De-init a pin. 
 * 
 * @param[in]   port    pointer to pin's Port data structure
 * 
 * @param[in]   pin     pin to de-init
 * 
 * @details Set pin to High-Z mode but doesn't disable clock.
 * 
 * @retval None
 * 
 */
void HAL_GPIO_deinit_pin(Hal_gpio_port_t* port, Hal_gpio_pin_t pin)
{
    HAL_ASSERT(port != NULL);

    Cy_GPIO_Pin_Init(port, pin, &default_config);
}

/**
 * @brief Read a digital pin state.
 * 
 * @param[in]   port    pointer to pin's Port data structure
 * 
 * @param[in]   pin     pin to read
 * 
 * @retval HAL_GPIO_LOW_LEVEL or HAL_GPIO_HIGH_LEVEL
 * 
 */
Hal_gpio_pin_state_t HAL_GPIO_read_pin(Hal_gpio_port_t* port, Hal_gpio_pin_t pin)
{
    HAL_ASSERT(port != NULL);

    Hal_gpio_pin_state_t state = (Hal_gpio_pin_state_t) Cy_GPIO_Read(port, pin);

    return state;
}

/**
 * @brief Set a digital pin state. 
 * 
 * @param[in]   port    pointer to pin's Port data structure
 * 
 * @param[in]   pin     pin to set
 * 
 * @param[in]   level   LOW or HIGH
 * 
 * @retval HAL_GPIO_LOW_LEVEL or HAL_GPIO_HIGH_LEVEL
 * 
 */
void HAL_GPIO_set_pin(Hal_gpio_port_t* port, Hal_gpio_pin_t pin, Hal_gpio_pin_state_t level)
{
    HAL_ASSERT(port != NULL);
    HAL_ASSERT(level <= HAL_GPIO_HIGH_LEVEL);

    Cy_GPIO_Write(port, (uint32_t) pin, (uint32_t) level);
}

